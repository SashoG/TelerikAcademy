using System;


class FloatvsDouble
{
    static void Main()
    {
        double a = 34.567839023d;
        float b = 12.354f;
        double c = 8923.1234857d;
        float d = 3456.091f;
        Console.WriteLine("a = " + a);
        Console.WriteLine("b = " + b);
        Console.WriteLine("c = " + c);
        Console.WriteLine("d = " + d);
    }
}

